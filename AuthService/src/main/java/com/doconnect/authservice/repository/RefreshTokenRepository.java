package com.doconnect.authservice.repository;

import com.doconnect.authservice.entity.RefreshToken;
import com.doconnect.authservice.entity.UserCredential;
import org.springframework.data.jpa.repository.JpaRepository;

import java.util.List;
import java.util.Optional;

public interface RefreshTokenRepository extends JpaRepository<RefreshToken, Long> {
    Optional<RefreshToken> findByToken(String token);
    List<RefreshToken> findByUserAndRevokedFalse(UserCredential user);
}
